// ds-author-decompose.js
// HTML-first design: AI authors a comprehensive design.html, then decomposes
// it to extract tokens.css and React primitives. The design.html IS the
// reference-example.html — single source of truth.
//
// Why this approach: AI generates better, more consistent designs when
// composing visual HTML/CSS first. Tokens extracted from real visual output
// are more coherent than tokens written abstractly.
//
// Usage: workflow('ds-author-decompose', { id, designMdPath, tasteDials? })
//   id           - kebab-case id for the design system
//   designMdPath - path to DESIGN.md (generated by ds-taste-profile or manual)
//   tasteDials   - optional { variance, motion, density } from ds-taste-profile
export const meta = {
  name: 'ds-author-decompose',
  description: 'HTML-first design: AI authors comprehensive design.html, then decomposes to tokens.css + React primitives.',
  phases: [
    { title: 'Author HTML', detail: 'Create comprehensive visual design.html from DESIGN.md + taste' },
    { title: 'Decompose → Tokens', detail: 'Extract colors, fonts, spacing, shadows from HTML → tokens.css' },
    { title: 'Discover Components', detail: 'Identify UI patterns in HTML → generate React primitives' },
  ],
}

const { id, designMdPath, tasteDials: dials } = args
if (!id) throw new Error('ds-author-decompose: id is required')
if (!designMdPath) throw new Error('ds-author-decompose: designMdPath is required')

const dsDir = `design-systems/${id}`
const outPath = `${dsDir}/reference-example.html`

// Ensure directory exists
try { await $`mkdir -p ${dsDir}` } catch { /* */ }

// Load taste context
let tasteContext = ''
let tasteDials = dials || { variance: 5, motion: 5, density: 5 }
if (!dials) {
  try {
    const tasteSkill = await $`cat ${dsDir}/skills/taste/SKILL.md 2>/dev/null`
    if (tasteSkill) {
      const vMatch = tasteSkill.match(/DESIGN_VARIANCE:\s*(\d+)/)
      const mMatch = tasteSkill.match(/MOTION_INTENSITY:\s*(\d+)/)
      const dMatch = tasteSkill.match(/VISUAL_DENSITY:\s*(\d+)/)
      if (vMatch) tasteDials.variance = parseInt(vMatch[1])
      if (mMatch) tasteDials.motion = parseInt(mMatch[1])
      if (dMatch) tasteDials.density = parseInt(dMatch[1])
      tasteContext = tasteSkill
    }
  } catch { /* no taste skill */ }
  if (!tasteContext) {
    try {
      tasteContext = await $`cat .claude/skills/design-taste/THE_DIALS.md 2>/dev/null`
    } catch { /* no taste system */ }
  }
}

// Read DESIGN.md
let designMd = ''
try {
  designMd = await $`cat ${designMdPath}`
  log(`[ds-author-decompose] DESIGN.md: ${designMd.length} chars`)
} catch {
  log(`[ds-author-decompose] ⚠️  Could not read DESIGN.md at ${designMdPath}`)
}

// =========================================================================
// Phase 1: Author Comprehensive HTML Design
// =========================================================================
phase('Author HTML')
log(`[ds-author-decompose] Authoring design.html for "${id}"`)
log(`[ds-author-decompose] Taste dials: V${tasteDials.variance} / M${tasteDials.motion} / D${tasteDials.density}`)

const authorResult = await agent(
  `You are a design engineer with impeccable visual taste. Your job: author a complete, self-contained HTML page that brings a design system to life visually.

This HTML will be the SINGLE SOURCE OF TRUTH for the design system. It will later be DECOMPOSED to extract tokens.css and React primitives. So be precise and thorough.

## Design System Identity
- ID: "${id}"
- DESIGN.md: "${designMdPath}"

## Design Taste Dials
- DESIGN_VARIANCE: ${tasteDials.variance}/10 (${tasteDials.variance <= 3 ? 'Conventional, safe' : tasteDials.variance <= 6 ? 'Characterful, one distinctive move' : 'Bold, experimental, opinionated'})
- MOTION_INTENSITY: ${tasteDials.motion}/10 (${tasteDials.motion <= 3 ? 'Static, minimal' : tasteDials.motion <= 6 ? 'Purposeful micro-interactions' : 'Expressive, animated'})
- VISUAL_DENSITY: ${tasteDials.density}/10 (${tasteDials.density <= 3 ? 'Airy, generous whitespace' : tasteDials.density <= 6 ? 'Balanced, comfortable' : 'Compact, efficient'})

${tasteContext ? `## Taste Reference\n${tasteContext.slice(0, 1000)}` : ''}

## DESIGN.md Content
${designMd ? '```\n' + designMd.slice(0, 4000) + '\n```' : 'No DESIGN.md available — use taste dials and infer the design direction'}

## Requirements for the HTML

### Structure
Create a single, complete, self-contained HTML file. All CSS in a single <style> block. NO external CSS. NO JavaScript needed to render.

Use these semantic class prefixes so the HTML can be decomposed later:
- \`.color-swatch\`, \`.color-swatch-bar\`, \`.color-hex\`, \`.color-role\` — for color palette items
- \`.type-sample\`, \`.type-label\`, \`.type-display\`, \`.type-body\`, \`.type-mono\` — for typography
- \`.spacing-row\`, \`.spacing-label\`, \`.spacing-bar\`, \`.spacing-value\` — for spacing scale
- \`.shadow-card\`, \`.shadow-label\` — for shadow samples
- \`.component-demo\`, \`.demo-button\`, \`.demo-card\`, \`.demo-input\`, \`.demo-badge\` — for component demos
- Add \`data-token="color-{name}"\` attributes on color swatches for easy extraction

### Sections (in order):

1. **HERO** — System name (large heading), description, category badge. Use the DISPLAY font (largest, boldest font in the page).

2. **COLOR PALETTE** — Grid of color swatch cards. Each card:
   - A colored bar (the actual color as background)
   - The hex value below it (monospace)
   - The semantic role name
   - data-token attribute like \`data-token="color-accent"\`
   Include: accent, surface, surface-raised, text, text-muted, border, success, warning, danger. Plus any additional colors from the DESIGN.md.
   Use ACTUAL colors that fit the design taste — don't use placeholder colors.

3. **TYPOGRAPHY** — Font specimens:
   - Display font (largest, boldest — 48px+ for the sample)
   - Body font (regular size, readable — 16-18px for the sample)
   - Mono font (monospace, code-style — 14px for the sample)
   For each: font name label, actual CSS font-family applied, a preview sentence.
   Use REAL font-family values (Inter, system-ui, serif, monospace, or custom).
   If the DESIGN.md names specific fonts, use those. Otherwise infer from taste dials.

4. **SPACING SCALE** — Visual bars showing spacing sizes:
   - xs (smallest), sm, md, lg, xl, 2xl (largest)
   - Each row: label | colored bar (width proportional to value) | value in px
   - Derive from the density dial: ${tasteDials.density <= 3 ? '8px unit' : tasteDials.density <= 6 ? '6px unit' : '4px unit'}

5. **SHADOWS** — If the design uses shadows, show 2-3 shadow cards with actual box-shadow applied.

6. **COMPONENT DEMOS** — This is critical. Show real, styled HTML/CSS for these components:
   - **Button** — primary variant (accent background), secondary (outline), ghost (no background). Include hover effects via :hover in CSS.
   - **Card** — container with border, padding, optional shadow. Show header + body.
   - **Input** — text input with label, placeholder text, focus border style
   - **Badge** — small label with colored background (accent variant, success variant)
   Each must use the SAME colors, fonts, and radii defined in the color palette and typography sections above.
   Use data-role attributes: \`data-role="button"\`, \`data-role="card"\`, \`data-role="input"\`, \`data-role="badge"\`

7. **FOOTER** — "Generated by emdesign · HTML-first design"

### CSS Style Requirements
- Google Fonts: include \`https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700;800&display=swap\` via @import or link
- Use consistent, clean spacing between sections
- Each section should be visually distinct with section headers
- Use the accent color sparingly but consistently
- Follow the density dial for spacing: ${tasteDials.density <= 3 ? 'generous padding (48px+), large gaps' : tasteDials.density <= 6 ? 'comfortable padding (32px), moderate gaps' : 'compact padding (16px), small gaps'}
- Follow the variance dial for shapes: ${tasteDials.variance <= 3 ? 'conservative border-radius (4-6px)' : tasteDials.variance <= 6 ? 'characterful radius (8-12px)' : 'bold radius (16px+ or 0px)'}

## Output
Write the complete HTML to "${outPath}" using \`cat > file << 'HTMLEOF'\`.

Return the file path and the number of unique colors used.`,
  { label: `author:${id}`, phase: 'Author HTML', schema: {
    type: 'object',
    properties: {
      filePath: { type: 'string' },
      uniqueColors: { type: 'number' },
      fontCount: { type: 'number' },
      componentTypes: { type: 'array', items: { type: 'string' } },
    },
    required: ['filePath'],
  }}
)

if (!authorResult?.filePath) {
  throw new Error('[ds-author-decompose] HTML authoring failed')
}

let finalSize = 0
try {
  const sizeStr = await $`wc -c < ${outPath} 2>/dev/null || echo 0`
  finalSize = parseInt(sizeStr.trim())
} catch { /* */ }
log(`[ds-author-decompose] ✅ design.html written: ${finalSize} bytes`)
log(`[ds-author-decompose]   Colors: ${authorResult.uniqueColors || '?'} · Fonts: ${authorResult.fontCount || '?'} · Components: ${(authorResult.componentTypes || []).join(', ') || '?'}`)

// =========================================================================
// Phase 2: Decompose HTML → tokens.css
// =========================================================================
phase('Decompose → Tokens')
log(`[ds-author-decompose] Decomposing design.html to extract tokens`)

// Copy design.html to DESIGN.md location for reference
try { await $`cp ${outPath} ${dsDir}/design.html 2>/dev/null` } catch { /* */ }

const tokensResult = await agent(
  `You are a design token extractor. Read the design HTML at "${outPath}" and extract ALL design values to generate a complete tokens.css file.

## What to extract

### 1. Colors
Read the design.html and find every unique color value used in:
- \`background\` properties (in <style> blocks and inline styles)
- \`color\` properties (text colors)
- \`border-color\` properties
- \`.color-swatch-bar\` background colors

For each unique color, determine its semantic role by:
- Looking at its \`data-token\` attribute if present (e.g. \`data-token="color-accent"\`)
- If no data-token, infer from context:
  - The most prominent/accent color → \`--color-accent\`
  - The page background → \`--color-surface\`
  - The main text color → \`--color-text\`
  - A lighter background variant → \`--color-surface-raised\`
  - Muted/secondary text → \`--color-text-muted\`
  - Border colors → \`--color-border\`
  - Green/red/amber status colors → \`--color-success\`, \`--color-danger\`, \`--color-warning\`
  - Blue info → \`--color-info\`

### 2. Typography
Find every \`font-family\` used. Categorize:
- The display/heading font → \`--font-display\` (largest, boldest usage)
- The body/text font → \`--font-sans\` (most common usage)
- A monospace font if used → \`--font-mono\`

### 3. Spacing
Find repeated \`padding\`, \`margin\`, \`gap\`, \`width\` values.
- Find the smallest repeated unit (e.g. 4px, 6px, 8px) → base unit
- Build a scale: xs, sm, md, lg, xl, 2xl
- Each maps to \`--space-{name}\`

### 4. Border Radius
Find all \`border-radius\` values. The most commonly used → \`--radius\`.
If there are multiple distinct radii, create \`--radius-sm\`, \`--radius-md\`, \`--radius-lg\`.

### 5. Shadows
Find all \`box-shadow\` values. The smallest → \`--shadow-sm\`, next → \`--shadow-md\`, largest → \`--shadow-lg\`.

### 6. Motion
Find any \`transition\` or \`animation\` values. Extract:
- Default duration → \`--motion-duration-default\`
- Default easing → \`--motion-easing-default\`

## Output format
Generate a complete tokens.css file with ALL extracted values. Use this exact format:

\`\`\`css
:root {
  /* === Colors === */
  --color-accent: #...;  /* description of usage */
  --color-surface: #...;
  /* ... etc */

  /* === Typography === */
  --font-display: "...", system-ui, sans-serif;
  --font-sans: "...", system-ui, sans-serif;
  --font-mono: "...", monospace;

  /* === Spacing === */
  --space-xs: ...;
  --space-sm: ...;
  /* ... etc */

  /* === Radii & Shadows === */
  --radius: ...;
  --shadow-sm: ...;
  /* ... etc */

  /* === Motion === */
  --motion-duration-default: ...;
  --motion-easing-default: ...;
}
\`\`\`

Write the tokens.css to "${dsDir}/tokens.css" using \`cat > file << 'CSSEOF'\`.

Return the number of tokens extracted and the categories found.`,
  { label: `tokens:${id}`, phase: 'Decompose → Tokens', schema: {
    type: 'object',
    properties: {
      tokensCssPath: { type: 'string' },
      tokenCount: { type: 'number' },
      categories: { type: 'array', items: { type: 'string' } },
    },
    required: ['tokensCssPath'],
  }}
)

if (tokensResult?.tokenCount) {
  log(`[ds-author-decompose] ✅ tokens.css generated: ${tokensResult.tokenCount} tokens across ${(tokensResult.categories || []).join(', ')}`)
} else {
  log(`[ds-author-decompose] ⚠️  Token extraction may be incomplete`)
}

// Verify tokens.css was written
let tokenSize = 0
try {
  const sizeStr = await $`wc -c < ${dsDir}/tokens.css 2>/dev/null || echo 0`
  tokenSize = parseInt(sizeStr.trim())
} catch { /* */ }
if (tokenSize === 0) {
  log(`[ds-author-decompose] ⚠️  tokens.css not written, attempting fallback`)
  try {
    const atelierTokens = await $`cat design-systems/atelier/tokens.css 2>/dev/null`
    if (atelierTokens) {
      const genericTokens = atelierTokens.replace(/atelier/g, id)
      await $`cat > ${dsDir}/tokens.css << 'CSSEOF'
${genericTokens}
CSSEOF`
      log(`[ds-author-decompose] Used atelier tokens.css as fallback`)
    }
  } catch { /* */ }
}

// =========================================================================
// Phase 3: Discover Components → Primitives
// =========================================================================
phase('Discover Components')
log(`[ds-author-decompose] Identifying UI patterns in HTML and generating primitives`)

const primitivesResult = await agent(
  `You are a component discoverer. Read the design HTML at "${outPath}" and the tokens.css at "${dsDir}/tokens.css".

## Identify UI Patterns
Look at the HTML structure and identify reusable component patterns:
1. **Button** — look for elements with \`data-role="button"\` or styled button-like elements. Extract: background colors, padding, border-radius, font-size, hover states.
2. **Card** — look for \`data-role="card"\` or container elements with border/padding/shadow. Extract: background, border, padding, shadow, border-radius.
3. **Input** — look for \`data-role="input"\` or form input-like elements. Extract: border, padding, font-size, focus styles.
4. **Badge** — look for \`data-role="badge"\` or small label/tag elements. Extract: font-size, padding, background, border-radius.
5. **Any other pattern** — if you see a repeating visual element that looks like a component (tabs, navigation, avatar, etc.), identify it too.

## Generate React Components
For each identified pattern, generate a React .tsx component file that:
- Uses \`var(--token-name)\` references from tokens.css (e.g. \`var(--color-accent)\`, \`var(--radius)\`)
- Has proper TypeScript prop types
- Exports with JSDoc comment
- Matches the HTML design's visual appearance

Write each file to "${dsDir}/code/{Name}.tsx" using \`cat > file << 'EOF'\`.

Create these components:
1. **Button.tsx** — variants: primary, secondary, ghost. Sizes: sm, md, lg. Support disabled, loading states. Use hover from the HTML design.
2. **Card.tsx** — container with optional header, body, footer. Use the styling from the HTML.
3. **Input.tsx** — text input with label, placeholder, error state. Use the styling from the HTML.
4. **Badge.tsx** — small label with color variants (accent, success, warning, danger). Use the styling from the HTML.

If the HTML design shows additional component patterns (tabs, navigation, toggle, avatar, etc.), create those too.

Each component MUST:
- Use inline style objects or className strings referencing CSS custom properties
- NOT hardcode hex values — always reference \`var(--color-*)\`, \`var(--font-*)\`, \`var(--space-*)\`, \`var(--radius)\`, etc.
- Include a brief JSDoc comment explaining the component

Return a list of all component files created.`,
  { label: `primitives:${id}`, phase: 'Discover Components', schema: {
    type: 'object',
    properties: {
      files: { type: 'array', items: { type: 'string' } },
      patterns: { type: 'array', items: { type: 'string' } },
    },
    required: ['files'],
  }}
)

if (primitivesResult?.files?.length) {
  log(`[ds-author-decompose] ✅ Primitives generated: ${primitivesResult.files.join(', ')}`)
  log(`[ds-author-decompose]   Patterns found: ${(primitivesResult.patterns || []).join(', ')}`)
} else {
  log(`[ds-author-decompose] ⚠️  No primitives generated`)
}

// Count created primitives
let codeCount = 0
try {
  await $`mkdir -p ${dsDir}/code 2>/dev/null`
  const count = await $`ls ${dsDir}/code/*.tsx 2>/dev/null | wc -l`
  codeCount = parseInt(count.trim() || '0')
} catch { /* */ }

// Write manifest.json
const manifest = {
  id,
  name: id,
  version: '0.1.0',
  category: 'Custom',
  description: `Generated via HTML-first design workflow`,
  files: { design: 'DESIGN.md', tokens: 'tokens.css', preview: 'reference-example.html', components: 'code/' },
  craft: { applies: ['off-token-color', 'accent-overuse'], exemptions: [] },
  stats: { tokens: tokensResult?.tokenCount || 0, primitives: codeCount },
  updatedAt: new Date().toISOString(),
}
try {
  await $`cat > ${dsDir}/manifest.json << 'JSONEOF'
${JSON.stringify(manifest, null, 2)}
JSONEOF`
  log(`[ds-author-decompose] ✅ manifest.json written`)
} catch (e) {
  log(`[ds-author-decompose] ⚠️  manifest write failed: ${e.message}`)
}

// =========================================================================
// Summary
// =========================================================================
log(`[ds-author-decompose] ✅ Complete: "${id}"`)
log(`[ds-author-decompose]   Preview: ${outPath} (${finalSize} bytes)`)
log(`[ds-author-decompose]   Tokens: ${dsDir}/tokens.css (${tokensResult?.tokenCount || '?'} tokens)`)
log(`[ds-author-decompose]   Primitives: ${codeCount} components in code/`)

return {
  id,
  path: dsDir,
  preview: outPath,
  tokens: `${dsDir}/tokens.css`,
  primitives: codeCount,
  tokenCount: tokensResult?.tokenCount || 0,
  colors: authorResult?.uniqueColors || 0,
  components: primitivesResult?.files || [],
  categories: tokensResult?.categories || [],
}
